/**
 *  main - prints all the numbers of base 16 in lowercase
 *
 *   Return: Always 0 (Success)
 */

int main(void)

{

		int a;

			char alpha;


				for (a = 48; a < 58; a++)

						{

									putchar(a);

										}

					for (alpha = 'a'; alpha <= 'f'; alpha++)

							{

										putchar(alpha);

											}

						putchar('\n');

							return (0);

}
